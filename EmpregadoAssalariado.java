package prova01;

public class EmpregadoAssalariado extends Empregado{

	private double salarioMensal;
	
	public EmpregadoAssalariado (String n, String s, String d, double sm) {
		super(n, s, d);
		this.salarioMensal = sm;
	}
	
	@Override
	public double salario() {
		return this.salarioMensal;
	}

	public double getSalarioMensal() {
		return salarioMensal;
	}

	public void setSalarioMensal(double salarioMensal) {
		this.salarioMensal = salarioMensal;
	}

	@Override
	public String toString() {
		return "EmpregadoAssalariado [salarioMensal=" + salarioMensal + ", getNome()=" + getNome() + ", getSobrenome()="
				+ getSobrenome() + ", getDocumento()=" + getDocumento() + "]";
	}
}

